#include "./libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int	r;

	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	r = (unsigned char)(*s1) - (unsigned char)(*s2);
	return (r);
}
