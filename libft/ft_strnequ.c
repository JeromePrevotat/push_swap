#include "./libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (*s1 && *s2 && i < n)
	{
		if (*s1 == *s2 && i < n)
		{
			s1++;
			s2++;
		}
		else
			return (0);
		i++;
	}
	return (1);
}
