#include "./libft.h"
#include <stdio.h>

long	ft_bonus_pow(int nb, int p)
{
	long	r;

	r = nb;
	if (p == 0)
		return (r = 1);
	while (p > 1)
	{
		r = r * nb;
		p--;
	}
	return (r);
}
