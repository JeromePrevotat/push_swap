#include "./libft.h"

int	ft_bonus_isprime(int nb)
{
	int	i;

	i = nb - 1;
	if (nb < 1)
		return (0);
	while (i > 1)
	{
		if (nb % i == 0)
			return (0);
		i--;
	}
	return (1);
}
